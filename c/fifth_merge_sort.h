void fifthMergeSort(void *array, size_t nelements, size_t elemsize, FIFTHCMP *cmp)
{
	if (nelements < 2)
	{
		return;
	}

	switch (elemsize)
	{
		case sizeof(char):
			return fifthMergeSort8(array, nelements, cmp);

		case sizeof(short):
			return fifthMergeSort16(array, nelements, cmp);

		case sizeof(int):
			return fifthMergeSort32(array, nelements, cmp);

		case sizeof(long long):
			return fifthMergeSort64(array, nelements, cmp);

#ifndef _WIN32
		case sizeof(long double):
			return fifthMergeSort128(array, nelements, cmp);
#endif

		default:
			return assert(elemsize == sizeof(char) || elemsize == sizeof(short) || elemsize == sizeof(int) || elemsize == sizeof(long long) || elemsize == sizeof(long double));
	}
}

